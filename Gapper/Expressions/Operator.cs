
namespace Gapper.Expressions
{
    public enum Operator
    {
        /// <summary>
        /// Equal =
        /// </summary>
        Eq,
        /// <summary>
        /// Not equal <>
        /// </summary>
        Nq,
        /// <summary>
        /// GreaterThan >
        /// </summary>
        Gt,
        /// <summary>
        /// GreaterThan or Equal >=
        /// </summary>
        Gte,
        /// <summary>
        /// LessThan <
        /// </summary>        
        Lt,
        /// <summary>
        /// LessThan or Equal <=
        /// </summary>
        Lte
    }
}
